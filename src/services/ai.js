// 后备翻译函数（当 API 不可用时使用简单的单词替换）
function simpleTranslate(sentence) {
  if (!sentence) return ''
  let result = sentence.toLowerCase()
  Object.keys(translations).forEach(word => {
    const regex = new RegExp(`\\b${word}\\b`, 'gi')
    result = result.replace(regex, translations[word])
  })
  return result.replace(/\s+/g, ' ').trim() || '暂无翻译'
}
