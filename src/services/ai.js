// Free Dictionary API - 免费获取单词释义和音标
const DICTIONARY_API = 'https://api.dictionaryapi.net/api/v3/entries/en'

/**
 * 获取单词基础信息（释义 + 音标）
 * @param {string} word - 单词
 */
export async function fetchWordDetails(word) {
  try {
    const response = await fetch(`${DICTIONARY_API}/${encodeURIComponent(word.toLowerCase())}`)
    if (!response.ok) throw new Error('API response not ok')

    const data = await response.json()
    console.log('Dictionary API 返回:', data)

    if (!Array.isArray(data) || data.length === 0) {
      // 如果 API 没有返回数据，尝试使用本地词典
      return getLocalDefinition(word)
    }

    const entry = data[0]
    const phonetic = entry.phonetic || entry.phonetics?.[0]?.text || ''
    const meanings = entry.meanings || []

    // 提取释义
    const definitions = meanings.map(m => {
      const partOfSpeech = m.partOfSpeech || 'n.'
      const defs = (m.definitions || []).slice(0, 3).map((d, i) =>
        `${i + 1}. ${d.definition}`
      ).join('\n')
      return `【${partOfSpeech}】\n${defs}`
    }).join('\n\n')

    return {
      phonetic: phonetic || '',
      definition: definitions || '暂无释义'
    }
  } catch (error) {
    console.error('Dictionary API Error:', error)
    // 使用本地离线词典作为后备
    return getLocalDefinition(word)
  }
}

